    private void popuniZvanja() {
        cmbZvanja.removeAllItems();
        cmbZvanja.addItem("Vandredni profesor");
        cmbZvanja.addItem("Redovni profesor");
        cmbZvanja.addItem("Docent");
    }

    private void popuniProfesore() {
        cmbProfesori.removeAllItems();
        Komunikacija.getInstance().posaljiZahtev(new Zahtev(Operacija.VRATI_PROFESORE, null));
        Odgovor odgovor = Komunikacija.getInstance().primiOdgovor();
        if (odgovor.getParametar() == null) {
            JOptionPane.showMessageDialog(this, odgovor.getPoruka());
        } else {
            ArrayList<Profesor> profesori = (ArrayList<Profesor>) odgovor.getParametar();
            for (Profesor profesor : profesori) {
                cmbProfesori.addItem(profesor);
            }
        }
    }

