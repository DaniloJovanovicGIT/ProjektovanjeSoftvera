    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String ime = txtIme.getText();
        String prezime = txtPrezime.getText();
        String zvanje = (String) cmbZvanja.getSelectedItem();
        Profesor noviProfesor = new Profesor(-1, ime, prezime, zvanje, ulogovani.getUsername(), null);
        Komunikacija.getInstance().posaljiZahtev(new Zahtev(Operacija.SACUVAJ_PROFESORA, noviProfesor));
        Odgovor odgovor = Komunikacija.getInstance().primiOdgovor();
        if (odgovor.getParametar() == null) {
            JOptionPane.showMessageDialog(this, odgovor.getPoruka());
        } else {
            JOptionPane.showMessageDialog(this, "Uspesno sacuvano.");
        }
        popuniProfesore();
    }//GEN-LAST:event_jButton1ActionPerformed

