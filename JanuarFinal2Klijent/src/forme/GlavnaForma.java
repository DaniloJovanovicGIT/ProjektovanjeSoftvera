    public void popuniAngazovanjaProfesora(Profesor izabrani) throws HeadlessException {
        Komunikacija.getInstance().posaljiZahtev(new Zahtev(Operacija.VRATI_ANGAZOVANJA_ZA_PROFESORA, izabrani));
        Odgovor odgovor = Komunikacija.getInstance().primiOdgovor();
        if (odgovor.getParametar() == null) {
            JOptionPane.showMessageDialog(this, odgovor.getPoruka());
        } else {
            System.out.println("Primljena angazovanja");
            ArrayList<Angazovanje> listaAngazovanja = (ArrayList<Angazovanje>) odgovor.getParametar();
            ModelTabeleAngazovanja mta = (ModelTabeleAngazovanja) tblAngazovanja.getModel();
            mta.setListaAngazovanja(listaAngazovanja);
        }
    }

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        ModelTabeleAngazovanja mta = (ModelTabeleAngazovanja) tblAngazovanja.getModel();
        ArrayList<Angazovanje> listaAngazovanja = mta.getListaAngazovanja();
        int brojacAngazovanja = 0;
        for (Angazovanje angazovanje : listaAngazovanja) {
            if (angazovanje.getStanje().equals("novo") || angazovanje.getStanje().equals("izmenjeno")) {
                brojacAngazovanja++;
            }
        }
        if (brojacAngazovanja < 3) {
            Profesor profesor = (Profesor) cmbProfesori.getSelectedItem();
            DodajAngazovanje da = new DodajAngazovanje(this, rootPaneCheckingEnabled, profesor);
            da.setVisible(true);
            da.setLocationRelativeTo(null);
        } else {
            JOptionPane.showMessageDialog(this, "Profesor ima maksimalno 3 angazovanja.");
        }


    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int row = tblAngazovanja.getSelectedRow();
        if (row != -1) {
            ModelTabeleAngazovanja mta = (ModelTabeleAngazovanja) tblAngazovanja.getModel();
            mta.removeRow(row);
        } else {
            JOptionPane.showMessageDialog(this, "Odaberi angazovanje");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int row = tblAngazovanja.getSelectedRow();
        if (row != -1) {
            ModelTabeleAngazovanja mta = (ModelTabeleAngazovanja) tblAngazovanja.getModel();
            Angazovanje angazovanje = mta.getAngazovanje(row);
            IzmeniAngazovanje ia = new IzmeniAngazovanje(this, rootPaneCheckingEnabled, angazovanje);
            ia.setVisible(true);
            ia.setLocationRelativeTo(null);

        } else {
            JOptionPane.showMessageDialog(this, "Odaberi angazovanje");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

