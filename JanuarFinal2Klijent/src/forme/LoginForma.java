    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Korisnik novi = new Korisnik("", "", txtUser.getText(), String.valueOf(txtPass.getPassword()));
        Komunikacija.getInstance().posaljiZahtev(new Zahtev(Operacija.LOGIN, novi));
        Odgovor odgovor = Komunikacija.getInstance().primiOdgovor();
        System.out.println("Odgovor primljen");
        if(odgovor.getParametar()==null){
            brojPokusaja--;
            JOptionPane.showMessageDialog(this, odgovor.getPoruka()+"\nPreostalo jos "+brojPokusaja+" pokusaja.");
        }else{
            Korisnik ulogovani = (Korisnik) odgovor.getParametar();
            GlavnaForma gf =  new GlavnaForma(ulogovani);
            gf.setVisible(true);
            gf.setLocationRelativeTo(null);
            this.dispose();
        }
        if(brojPokusaja==0){
        System.exit(0);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

