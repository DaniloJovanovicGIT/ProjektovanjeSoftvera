    private void popnuiKorisnike() {
        cmbZaKoga.removeAllItems();
        Komunikacija.getInstance().posaljiZahtev(new Zahtev(Operacija.VRATI_ULOGOVANE, null));
    }

    public void obradiUlogovane(ArrayList<String> listaUlogovanih) {
        System.out.println("popunjavam ulogovane");
        cmbZaKoga.removeAllItems();
        cmbZaKoga.addItem("svi");
        for (String string : listaUlogovanih) {
            cmbZaKoga.addItem(string);
        }
    }

    public void obradiPorukaPoslata(boolean uspesno) {
        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Uspesno poslato");
        } else {
            JOptionPane.showMessageDialog(this, "Doslo je do greske");
        }
    }

    private void srediForme() {
        tblPoslate.setModel(new ModeltabelePoruka());
        tblPrimeljene.setModel(new ModeltabelePoruka());
        popuniPrimljenePorukeZaKorisnika();
        popuniPoslatePorukeZaKorisnika();

    }

    private void popuniPrimljenePorukeZaKorisnika() {
        Komunikacija.getInstance().posaljiZahtev(new Zahtev(Operacija.VRATI_PRIMLJENE_PORUKE_ZA_KORISNIKA, user));
    }

    public void obradiPrimljene(ArrayList<Poruka> listaPrimljenih) {
        ModeltabelePoruka mta = (ModeltabelePoruka) tblPrimeljene.getModel();
        mta.setListaPoruka(listaPrimljenih);
    }

    private void popuniPoslatePorukeZaKorisnika() {
        Komunikacija.getInstance().posaljiZahtev(new Zahtev(Operacija.VRATI_POSLATE_PORUKE_ZA_KORISNIKA, user));
    }

    public void obradiPoslate(ArrayList<Poruka> listaPoslatih) {
        ModeltabelePoruka mta = (ModeltabelePoruka) tblPoslate.getModel();
        mta.setListaPoruka(listaPoslatih);
    }

