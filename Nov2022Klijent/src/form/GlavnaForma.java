    private void btnPosaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPosaljiActionPerformed
        if (txtPoruke.getText().length() > 200) {
            JOptionPane.showMessageDialog(this, "Poruka ne sme imati vise od 200 karaktera");
        } else {
            String odKoga = user.getUsername();
            String zaKoga = (String) cmbZaKoga.getSelectedItem();
            String textPoruke = txtPoruke.getText();
            Date vremeSlanja = new Date();
            Poruka poruka = new Poruka(odKoga, zaKoga, textPoruke, vremeSlanja);
            Komunikacija.getInstance().posaljiZahtev(new Zahtev(Operacija.POSALJI_PORUKU, poruka));
        }

    }//GEN-LAST:event_btnPosaljiActionPerformed

