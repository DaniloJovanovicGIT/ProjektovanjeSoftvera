    private void btnPosaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPosaljiActionPerformed
        String kome = (String) cmbKome.getSelectedItem();
        String ko = korisnik.getKorisnickoIme();
        String text = txtPoruka.getText();
        Date datum = new Date();
        if (text.length() > 200) {
            JOptionPane.showMessageDialog(null, "Poruka moze sadrzati do 200 karaktera");
        }

        Poruka poruka = new Poruka(-1, ko, kome, datum, text);
        Zahtev zahtev = new Zahtev(Operacija.POSALJI_PORUKU, poruka);
        Komunikacija.getKomunikacija().posaljiZahtev(zahtev);
    }//GEN-LAST:event_btnPosaljiActionPerformed

