    void popuniKorisnike() {
        Komunikacija.getKomunikacija().posaljiZahtev(new Zahtev(Operacija.VRATI_ULOGOVANE_KORISNIKE, null));

    }

    public void popuniCMBKorisnika(ArrayList<String> listaKorisnika) {
        String lazni = "svima";
        cmbKome.removeAllItems();
        cmbKome.addItem(lazni);
        for (String string : listaKorisnika) {
            cmbKome.addItem(string);
        }
    }

    void vratiSvePoruke() {
        Zahtev zahtev = new Zahtev(Operacija.VRATI_PORUKE_ZA_KORISNIKA, null);
        Komunikacija.getKomunikacija().posaljiZahtev(zahtev);
    }

    public void popuniListuPoruka(ArrayList<Poruka> listaPoruka) {
        cmbSvePoruker.removeAllItems();
        cmbZadnjeTri.removeAllItems();
        int brojac = 0;
        for (Poruka poruka : listaPoruka) {
            if (brojac < 3) {
                cmbZadnjeTri.addItem(poruka);
            }
            cmbSvePoruker.addItem(poruka);
            brojac++;
        }
    }
