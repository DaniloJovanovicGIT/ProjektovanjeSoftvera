    private void popuniPolja() {
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        txtNaziv.setText(festival.getNaziv());
        txtDatumOd.setText(sdf.format(festival.getDatumOd()));
        txtDatumDo.setText(sdf.format(festival.getDatumDo()));
        txtOpis.setText(festival.getOpis());
    }

    private void srediTabelu() {
        tblIzvodjenja.setModel(new ModelTabeleIzvodjenja());
        ModelTabeleIzvodjenja mti = (ModelTabeleIzvodjenja) tblIzvodjenja.getModel();
        mti.setLista(festival.getListaIzvodjenja());
    }

    void prihvatiIzvodjenje(Izvodjenje izvodjenje) {
        ModelTabeleIzvodjenja mti = (ModelTabeleIzvodjenja) tblIzvodjenja.getModel();
        mti.add(izvodjenje);
    }
