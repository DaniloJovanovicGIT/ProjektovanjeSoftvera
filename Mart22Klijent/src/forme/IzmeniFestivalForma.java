    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        ModelTabeleIzvodjenja mti = (ModelTabeleIzvodjenja) tblIzvodjenja.getModel();
        if (!(txtNaziv.getText().isEmpty() && txtDatumDo.getText().isEmpty() && txtDatumOd.getText().isEmpty() && txtOpis.getText().isEmpty()) && !txtDatumOd.getText().equals(txtDatumDo.getText())) {
            Festival izmenjeniFestiva = festival;
            izmenjeniFestiva.setNaziv(txtNaziv.getText());
            try {
                izmenjeniFestiva.setDatumOd(sdf.parse(txtDatumOd.getText()));
                izmenjeniFestiva.setDatumDo(sdf.parse(txtDatumDo.getText()));
                izmenjeniFestiva.setOpis(txtOpis.getText());
                izmenjeniFestiva.setListaIzvodjenja(mti.getLista());
                Komunikacija.getInstanca().posaljiZahtev(new Zahtev(Operacija.IZMENI_FESTIVAL, izmenjeniFestiva));
                Odgovor odgovor = Komunikacija.getInstanca().primiOdgovor();
                if ((boolean) odgovor.getParametar() == false) {
                    JOptionPane.showMessageDialog(this, odgovor.getPoruka());
                } else {
                    JOptionPane.showMessageDialog(this, "Uspesno sacuvano");
                    festival = izmenjeniFestiva;
                }
                srediTabelu();
                this.dispose();
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(this, "Neispravnani format datuma.");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Tekst mora biti popunjen i vreme pocetka i kraja ne sme biti isto");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

