    private void btnSacuvajPredmetIAngazovanjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajPredmetIAngazovanjeActionPerformed
        Profesor profesor = (Profesor) cmbProfesoriPredmet.getSelectedItem();
        Komunikacija.getInstance().posaljiZahtev(new Zahtev(Operacija.BROJ_ANGAOVANJA_PROFESORA, profesor));
        Odgovor odg = Komunikacija.getInstance().primiOdgovor();
        int brojAngazovana = (int) odg.getParametar();
        if (brojAngazovana != -1 && brojAngazovana < 3) {
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            if (txtKodPredmeta.getText().length() == 10) {
                try {
                    int brojESPB = Integer.parseInt(txtBrojESPB.getText());
                    Predmet predmet = new Predmet(txtSifraPredmeta.getText(), txtNazivPredmeta.getText(), txtKodPredmeta.getText(), brojESPB);
                    Date datum = new Date();
                    datum = sdf.parse(txtDatum.getText());
                    Angazovanje angazovanje = new Angazovanje(-1, profesor, predmet, datum, ulogovani.getEmail(),"novo");
                    Komunikacija.getInstance().posaljiZahtev(new Zahtev(Operacija.SACUVAJ_ANGAZOVANJE_PREDMET, angazovanje));
                    Odgovor odgovor = Komunikacija.getInstance().primiOdgovor();
                    if (odgovor.getParametar() == null) {
                        JOptionPane.showMessageDialog(this, odgovor.getPoruka());
                    } else {
                        JOptionPane.showMessageDialog(this, "Uspesno sacuvano");
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Datum mora imati dd.MM.yyyy format i vrednost za broj EPSB mora bit broj.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Kod mora imati 10 kakatera");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Profesor moze imati samo 3 angazovanjaodgovor");
        }
        

    }//GEN-LAST:event_btnSacuvajPredmetIAngazovanjeActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Profesor izabrani = (Profesor) cmbProfesorAngazovanja.getSelectedItem();
        Komunikacija.getInstance().posaljiZahtev(new Zahtev(Operacija.VRATI_ANGAZOVANJA_ZA_PROFESORA, izabrani));
        Odgovor odg = Komunikacija.getInstance().primiOdgovor();
        if (odg.getParametar() == null) {
            JOptionPane.showMessageDialog(this, odg.getPoruka());
        } else {
            ArrayList<Angazovanje> lista = (ArrayList<Angazovanje>) odg.getParametar();
            ModelTabeleAngazovanja mta = (ModelTabeleAngazovanja) tblAngazovanja.getModel();
            mta.setLista(lista);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

