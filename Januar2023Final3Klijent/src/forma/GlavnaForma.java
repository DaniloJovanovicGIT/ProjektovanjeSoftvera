    private void popuniProfesore() {
        Komunikacija.getInstance().posaljiZahtev(new Zahtev(Operacija.VRATI_PROFESORE, null));
        Odgovor odg = Komunikacija.getInstance().primiOdgovor();
        cmbProfesoriPredmet.removeAllItems();
        cmbProfesorAngazovanja.removeAllItems();
        if (odg.getParametar() == null) {
            JOptionPane.showMessageDialog(this, odg.getPoruka());
        } else {
            ArrayList<Profesor> lista = (ArrayList<Profesor>) odg.getParametar();
            for (Profesor profesor : lista) {
                cmbProfesoriPredmet.addItem(profesor);
                cmbProfesorAngazovanja.addItem(profesor);
            }
        }
    }
