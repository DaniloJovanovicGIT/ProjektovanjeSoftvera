    private void popuniTipove() {
        cmbTip.removeAllItems();
        cmbTip.addItem("Autmobil");
        cmbTip.addItem("Kombi");
        cmbTip.addItem("Kamion");
        cmbTip.addItem("Autobus");
    }

    private void popuniDatum() {
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy hh:mm");
        Date now = new Date();

        txtPocetak.setText(String.valueOf(sdf.format(now)));
    }

    private void popuniPogone() {
        cmbPogon.removeAllItems();
        ArrayList<Pogon> lista;
        komunikacija.Komunikacija.getInstance().posaljiZahtev(new KlijentskiZahtev(Operacija.VRATI_POGONE, null));
        ServerskiOdgovor so = komunikacija.Komunikacija.getInstance().primiOdgovor();
        lista = (ArrayList<Pogon>) so.getParametar();
        if (so == null) {
            JOptionPane.showMessageDialog(this, "Greska prilikom ucitavanja pogona");
        } else {

            for (Pogon pogon : lista) {
                cmbPogon.addItem(pogon);
            }
        }
    }

    void dodajKomponentu(Komponenta komponenta) {
        ModelTabeleKomponenti mtk = (ModelTabeleKomponenti) tblKomponente.getModel();
        mtk.add(komponenta);
    }
