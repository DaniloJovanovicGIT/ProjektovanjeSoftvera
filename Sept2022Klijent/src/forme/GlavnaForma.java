    private void btnSacuvajPrevoznoSredstvoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajPrevoznoSredstvoActionPerformed
        ModelTabeleKomponenti mtk =  (ModelTabeleKomponenti) tblKomponente.getModel();
        String model = txtModel.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy hh:mm");
        Date pocetakProizvodnje = new Date();
        Date zavretakProizvodnje= new Date();
        try {
            pocetakProizvodnje = sdf.parse(txtPocetak.getText());
            zavretakProizvodnje = sdf.parse(txtZavrsetak.getText());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Datum!");
        }
        int ocekivaniRadniVek = Integer.parseInt(txtOcekivaniVek.getText());
        String tip = (String) cmbTip.getSelectedItem();
        Pogon pogon = (Pogon) cmbPogon.getSelectedItem();
        Rukovodilac rukovodilac = session.Session.getInstance().getUlogovani();
        ArrayList<Komponenta> komponente = mtk.getLista();
        PrevoznoSredstvo ps =new PrevoznoSredstvo(-1, model, pocetakProizvodnje, zavretakProizvodnje, ocekivaniRadniVek, tip, pogon, rukovodilac, komponente);
        KlijentskiZahtev kz = new KlijentskiZahtev(Operacija.SACUVAJ_PREVOZNO_SREDSTVO, ps);
        komunikacija.Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = komunikacija.Komunikacija.getInstance().primiOdgovor();
        if((boolean)so.getParametar()==false){
        JOptionPane.showMessageDialog(this, "Doslo je do greske");
        }else{
        JOptionPane.showMessageDialog(this, "Uspesno");
        }
        mtk.setLista(new ArrayList<>());
    }//GEN-LAST:event_btnSacuvajPrevoznoSredstvoActionPerformed

