public class BrojProfesora {
    private String zvanje;
    private int broj;

    public BrojProfesora() {
    }

    public BrojProfesora(String zvanje, int broj) {
        this.zvanje = zvanje;
        this.broj = broj;
    }

    public String getZvanje() {
        return zvanje;
    }

    public void setZvanje(String zvanje) {
        this.zvanje = zvanje;
    }

    public int getBroj() {
        return broj;
    }

    public void setBroj(int broj) {
        this.broj = broj;
    }

   
}
